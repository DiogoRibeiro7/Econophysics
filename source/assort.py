import numpy as np


def assort(A):
    # Degree matrix
    D = np.sum(A,1)
    
    # Total number of edges
    T = int(np.sum(D)/2)
    
    # Number of nodes
    N = np.shape(A)[0]

    # Prob. edge from 
    e = np.array([[0.0 for i in range(max(D)+1)] for j in range(max(D)+1)])
    for i in range(N-1):
        for j in range(i+1,N):
            e[D[i],D[j]] += A[i,j]/T

    p = np.sum(np.dot(e,e))
    num = np.trace(e)-p
    den = 1-p
    
        
    if (np.trace(e) >= 0.9999):
        r = 1
    else:
        r = num/den
    
    return(r)


